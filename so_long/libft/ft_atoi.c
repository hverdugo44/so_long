#include "libft.h"

int	ft_atoi(const char *s)
{
	int	i;
	int	sig;
	int	res;

	i = 0;
	sig = 1;
	res = 0;
	while (s[i] == ' ' || (s[i] >= 9 && s[i] <= 13))
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			sig = sig * -1;
		i++;
	}
	while (s[i] >= 48 && s[i] <= 57)
	{
		res = (s[i] - 48) + (res * 10);
		i++;
	}
	return (res * sig);
}
